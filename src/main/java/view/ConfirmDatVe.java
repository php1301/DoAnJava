    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyReleased
        try {
            int val = Integer.parseInt(jTextField1.getText());
            if (val > diemTichLuyHienCo) {
                jLabel20.setText("Vui lòng dien diểm tích lũy bé hơn điểm tích lũy hiện có");
                btnMuaVe.setEnabled(false);
            } else {
                diemTichLuy = val;
                int sum = 0;
                listGiam.remove(0);
                listGiam.add(0, diemTichLuy * 10);
                for (int i : listGiam) {
                    sum += i;
                }
                soTienGiam = sum;
                int newTong = Integer.parseInt(thongTinXacNhan[4].toString().split(" ")[0]) - soTienGiam;
                giaTong.setText(newTong + " VND");
                jLabel16.setText(soTienGiam + " VND");
                jLabel11.setText(diemTichLuy + " điểm tương ứng với " + diemTichLuy * 10 + " VND");
                jLabel20.setText("");
                btnMuaVe.setEnabled(true);
            }
        } catch (NumberFormatException e) {
            btnMuaVe.setEnabled(false);
            jLabel20.setText("Vui lòng dien diem tích lũy hợp lệ");
        }    }//GEN-LAST:event_jTextField1KeyReleased

