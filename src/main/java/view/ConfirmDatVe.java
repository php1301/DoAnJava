    private void getThongTinKhachHang(int taiKhoanArg) {
        try {
            Object[] o = userController.getThongTinUser(taiKhoanArg);
            diemTichLuyHienCo = (Integer) o[6];
            jLabel9.setText(diemTichLuyHienCo + " điểm");
            Date birthday = (Date) o[4];
            Calendar cal = Calendar.getInstance();
            cal.setTime(birthday);
            LocalDate currentdate = LocalDate.now();
            int thangSinh = cal.get(Calendar.MONTH) + 1;
            int currentMonth = currentdate.getMonthValue();
            if (thangSinh == currentMonth) {
                jLabel13.setText("Chúc mừng! Đây là tháng sinh nhật của bạn - bạn được giảm 10% trên tiền tổng ban đầu");
                int phanTramTienGiam = Integer.parseInt(thongTinXacNhan[4].toString().split(" ")[0]) * 10 / 100;
                listGiam.add(phanTramTienGiam);
                int sum = 0;
                for (int i : listGiam) {
                    sum += i;
                }
                soTienGiam = sum;
                int newTong = Integer.parseInt(thongTinXacNhan[4].toString().split(" ")[0]) - soTienGiam;
                giaTong.setText(newTong + " VND");
                jLabel16.setText(soTienGiam + " VND");

            }

        } catch (SQLException ex) {
            Logger.getLogger(ConfirmDatVe.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

