    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        // TODO add your handling code here:
        DefaultTableModel SearchTable = (DefaultTableModel) tblPhong.getModel();
        String search = txtSearch.getText();
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<>(SearchTable);
        tblPhong.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(search));
    }//GEN-LAST:event_txtSearchKeyReleased
    private void createTablePhong() {
        try {
            List<Phong> phong = phongController.layDanhSachPhong();
            modelPhong = (DefaultTableModel) tblPhong.getModel();
            modelPhong.setRowCount(0);
            for (Phong p : phong) {
                Object[] o = new Object[2];
                o[0] = p.getMaRap();
                o[1] = p.getTenRap();
//                o[2] = p.getSoGhe();
//                o[3] = p.getMaCumRap();
                modelPhong.addRow(o);
            }
        } catch (SQLException ex) {
            Logger.getLogger(QLPhong.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

