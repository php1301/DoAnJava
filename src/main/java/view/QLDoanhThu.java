    private void renderComboBoxChonPhim() {
        try {
            phim = phimController.layDanhSachPhim();
            for (Phim p : phim) {
                tenPhim.add(p.getMaPhim() + " - " + p.getTenPhim());
                listmaphim.add(p.getMaPhim());
            }
            selectPhim.setModel(new DefaultComboBoxModel(tenPhim.toArray()));
        } catch (SQLException ex) {
            Logger.getLogger(QLDoanhThu.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
