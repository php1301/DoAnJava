    private void btnXuatdoanhthuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXuatdoanhthuActionPerformed
        Integer thang= Integer.parseInt (selectedThang.getSelectedItem().toString());
        int nam =selectedNam.getYear();       
        int maPhim= listmaphim.get(selectPhim.getSelectedIndex());
    try {
        List<DoanhThu> doanhthu = DoanhThuController.layDanhSachDoanhThu(thang, nam, maPhim);
        modelDoanhThu = (DefaultTableModel) tbdoanhthu.getModel();
        modelDoanhThu.setRowCount(0);
        System.out.println("Mã phim da chon: "+maPhim);
         for (DoanhThu dt : doanhthu) {
                Object[] o = new Object[3];
                o[0] = dt.getMaPhim();    
                o[1] = dt.getTenPhim();
                o[2] = dt.getDoanhThu();
                modelDoanhThu.addRow(o);
            }
    } catch (SQLException ex) {
        Logger.getLogger(QLDoanhThu.class.getName()).log(Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_btnXuatdoanhthuActionPerformed

